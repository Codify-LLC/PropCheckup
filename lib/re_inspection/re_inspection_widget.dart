                                          ),
                                          Row(
                                            mainAxisSize: MainAxisSize.max,
                                            mainAxisAlignment:
                                                MainAxisAlignment.spaceEvenly,
                                            children: [
                                              CachedNetworkImage(
                                                imageUrl:
                                                    'https://picsum.photos/seed/972/600',
                                                width: 100,
                                                height: 100,
                                                fit: BoxFit.cover,
                                              ),
